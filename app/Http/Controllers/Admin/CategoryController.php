    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Category $category)
    {
        if ($category->cards()->count() > 0) return back()->with('warning', 'Category cannot be removed!');
        // return $category;
        $category->delete();
        return back()->with('success', 'Category successfully removed!');
    }
